// message de supresion


let buttonsupresion = document.querySelectorAll('.deletee');
let modaleaficher = document.getElementById('confirmationModal');
let buttonAnuler = document.getElementById('cancelBtn');

buttonsupresion.forEach((button)=>{
    button.addEventListener('click' , ()=>{
        modaleaficher.style.display = 'block' ;
    })
})

buttonAnuler.addEventListener('click' , ()=>{
    modaleaficher.style.display = "none" ;
})


// anulation du modale l'orsque on click sur window
window.addEventListener('click' , (e)=>{
    if(e.target == modaleaficher){
        modaleaficher.style.display = "none";
    }
})









